export default function BlogPostArticle({ post }: { post: BlogPost }) {
  const { language } = useLanguage();
  const localized = language === "ar" ? arabicPosts[post.slug] : null;
  const sections = localized?.sections || post.sections;

  return (
    <article className="min-h-screen bg-white text-gray-900" dir={language === "ar" ? "rtl" : "ltr"}>
      <header className="bg-gradient-to-b from-[#0a112d] via-[#061640] to-[#0a112d] text-white">
        <div className="container mx-auto px-5 pt-32 pb-16">
          <Link
            href="/blog"
            className="inline-flex items-center gap-2 text-sm font-semibold text-[#dca958]"
          >
            <ArrowLeft className="h-4 w-4" />
            {language === "ar" ? "العودة إلى المدونة" : "Back to blog"}
          </Link>
          <div className="mt-8 flex flex-wrap items-center gap-3 text-xs uppercase tracking-wider text-white/50">
            <span>{localized?.category || post.category}</span>
            <span aria-hidden="true">/</span>
            <time dateTime={post.date}>
              {new Intl.DateTimeFormat(language === "ar" ? "ar-AE" : "en-AE", {
                month: "long",
                day: "numeric",
                year: "numeric",
              }).format(new Date(post.date))}
            </time>
            <span aria-hidden="true">/</span>
            <span>{localized?.readTime || post.readTime}</span>
          </div>
          <h1 className="mt-5 max-w-4xl text-4xl font-bold leading-tight md:text-6xl">
            {localized?.title || post.title}
          </h1>
          <p className="mt-6 max-w-3xl text-lg leading-8 text-white/70">
            {localized?.excerpt || post.excerpt}
          </p>
        </div>
      </header>

      <div className="container mx-auto max-w-4xl px-5 py-14">
        {sections.map((section) => (
          <section key={section.heading} className="mb-12">
            <h2 className="text-2xl font-bold text-[#0a112d]">{section.heading}</h2>
            <div className="mt-5 space-y-5">
              {section.paragraphs.map((paragraph) => (
                <p key={paragraph} className="text-lg leading-8 text-gray-700">
                  {paragraph}
                </p>
              ))}
            </div>
          </section>
        ))}
        <BlogCta />
      </div>
    </article>
  );
}
